public class Cheese {
    private int price;
    private String message;

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    @Override
    public String toString() {
        return "Cheese{" +
                "price=" + price +
                ", message='" + message + '\'' +
                '}';
    }
}
